
public class Byte {
  public static void main(String[] args) {
    
    /** 8-bit signed. */

    byte b1 = 100;
    byte b2 = 20;
    byte b3 = 128; // -127

    System.out.println("b1 = " + b1); // "b1 = 100"
    System.out.println("b2 = " + b2); // "b2 = 20"
    System.out.println("b3 = " + b3); // "b3 = -127"
  }
}
