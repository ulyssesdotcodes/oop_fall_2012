

public class IfElseIfElse {
  public static void main(String[] args) {
    
    boolean b = true;
    if (b) { System.out.println("true"); }

    int i = 10;
    if (i > 100)
      System.out.println("i > 100");
    else if (i > 50)
      System.out.println("i > 50");
    else
      System.out.println("i <= 50");

    // "i is less than 50"
  }
}

