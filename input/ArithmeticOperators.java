

public class ArithmeticOperators {
  public static void main(String[] args) {
    int i = 50 + 20;
    int j = i - 10;
    int k = j * 2;
    double l = k / 6;

    System.out.println("i = " + i); // "i = 70"
    System.out.println("j = " + j); // "j = 60"
    System.out.println("k = " + k); // "k = 120"
    System.out.println("l = " + l); // "l = 20.0"
  }
}
